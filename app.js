//ADD
function addNumbers(x, y){
    let sum = x + y;
    return sum;
}

//SUBTRACT
function subtract(x,y){
    let difference = x - y;
    return difference;
}

//MULTIPLY
function multiply(x,y){
    let product = x * y;
    return product;
}

//DIVIDE
function divide(x,y){
    let quotient = x/y;
    return quotient;
}

//RUN OPERATION
function operate(operator,x,y){
    let result = operator(x,y);
    return result;
}
